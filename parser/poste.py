class Poste:

    def __init__(self, id, x_cord, y_cord):
        self.id = id,
        self.x_cord = x_cord,                                                                                
        self.y_cord = x_cord,

        def fixToupleBug(self):
            self.id = self.id[0]
            self.x_cord = self.x_cord[0]
            self.y_cord = self.y_cord[0]

        fixToupleBug(self)


 


